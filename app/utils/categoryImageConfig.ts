// 图片文件扩展名优先级
export const IMAGE_EXTENSIONS = ['.png', '.jpg', '.jpeg', '.webp'];

// 默认图片配置
export const DEFAULT_CATEGORY_CONFIG = {
  // 是否在找不到图片时显示警告日志
  showWarningLogs: true,
  
  // 图片加载失败时的默认行为
  fallbackToNetworkImage: true,
};

/**
 * 获取所有已配置的类目ID
 */
export const getConfiguredCategoryIds = (): number[] => {
  return Object.keys(CATEGORY_IMAGE_MAP).map(id => parseInt(id, 10));
};

/**
 * 检查某个类目ID是否已配置本地图片
 */
export const isCategoryConfigured = (categoryId: number): boolean => {
  return categoryId in CATEGORY_IMAGE_MAP;
};

/**
 * 根据类目ID获取本地图片
 * 仅支持预配置在映射表中的图片
 */
export const getCategoryImage = (categoryId: number): any | null => {
  // 检查映射表中是否已配置
  if (categoryId in CATEGORY_IMAGE_MAP) {
    return CATEGORY_IMAGE_MAP[categoryId];
  }
  
  if (DEFAULT_CATEGORY_CONFIG.showWarningLogs) {
    console.warn(`类目图片未配置: ${categoryId}，请在CATEGORY_IMAGE_MAP中添加配置`);
  }
  
  return null;
};

/**
 * 批量生成类目图片映射的辅助函数
 * 注意：由于React Native限制，不支持动态导入
 * 如需添加新图片，请直接在CATEGORY_IMAGE_MAP中手动添加
 */
export const generateCategoryImageMap = (categoryIds: number[]): Record<number, any> => {
  const map: Record<number, any> = {};
  
  categoryIds.forEach(id => {
    if (id in CATEGORY_IMAGE_MAP) {
      map[id] = CATEGORY_IMAGE_MAP[id];
    } else {
      console.warn(`类目图片未配置: ${id}，请在CATEGORY_IMAGE_MAP中手动添加`);
    }
  });
  
  return map;
};
